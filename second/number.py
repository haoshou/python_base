# 数字
# 1.整数
# 在Python中，可对整数执行加（+）减（-）乘（*）除（/）运算
data = 2 + 3

data = 3 - 2

data = 3 * 2

data = 3 / 2

print(data)

# 在终端会话中，Python直接返回运算结果。Python使用两个乘号表示乘方运算：

data = 3 ** 2

print(data)

# 2.浮点数 Python将带小数点的数字都称为浮点数。
data = 0.1 + 0.2
print(data)

# 3.使用函数 str()避免类型错误
age = 23
# 需要将数字转为字符串
message = "Happy" + str(age) + "Birthday"
print(message)
